namespace Telerik.UI.Xaml.Controls.Data.DataForm.Commands
{
    /// <summary>
    /// Defines the known commands that are available within a <see cref="RadListView"/> control.
    /// </summary>
    public enum CommandId
    {
        /// <summary>
        /// A command associated with the request of a Commit operation on an item.
        /// </summary>
        Commit,

        /// <summary>
        /// A command associated with the request of a Validation operation over an item.
        /// </summary>
        Validate
    }
}
